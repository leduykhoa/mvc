class BaseModel
{
    public $table;

    function __construct($table)
    {
        $this->table = $table;
    }

    public function all($fetchType = PDO::FETCH_OBJ)
    {
        $db = DB::getInstance();
        $req = $db->query('SELECT * FROM ' . $this->table);
        return $req->fetchAll($fetchType);
    }
}
