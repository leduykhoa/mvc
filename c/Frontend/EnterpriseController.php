namespace App\Controllers\Frontend;

use App\Model\BaseModel;

class EnterpriseController extends FrontendController
{
    public function __construct()
    {
        parent::__construct();
    }

    public function index()
    {
        $obj = new BaseModel(plural('enterprise'));
        $enterprises = $obj->find();
        $data = ['enterprises' => $enterprises];
        $this->render('frontend/enterprise/index', $data);
    }

    public function detail($id)
    {
        $obj = new BaseModel(plural('enterprise'));
        $enterprise = $obj->findOne([
            'conditions' => [
                'id' => $id
            ]
        ]);
        if ($enterprise) {
            $data = ['enterprise' => $enterprise];
            $this->render('frontend/enterprise/detail', $data);
            return;
        }
        $this->render('frontend/error/404', []);
    }
}
