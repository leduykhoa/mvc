class ContactController extends BaseController
{
    public function __construct()
    {
    }

    public function index()
    {
        if (isPost() == true) {
            $obj = new BaseModel('tickets');
            $data = [
                'id' => Utils::genUuid(),
                'name' => request('name'),
                'email' => request('email'),
                'content' => request('content'),
            ];
            $result = $obj->insert(['data' => $data]);
        }
        $this->render('contact/index', $data);
    }
}
